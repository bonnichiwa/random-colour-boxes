import React, { Component } from 'react';
import Box from './Box';
import './App.css';

const NUM_BOX = 32;

class App extends Component {
  constructor(props) {
    super(props);
    const boxes = Array(NUM_BOX).fill().map(this.getRandomColor, this);
    this.state = {boxes};
    
    setInterval(() => {
      const randomBox = Math.floor(Math.random() * this.state.boxes.length);
      const boxes = [...this.state.boxes];
      boxes[randomBox] = this.getRandomColor();
      this.setState({boxes});
    }, 500);
  }
  
  changeColor(index) {
    console.log(this);
    if (this.props.index === index) {

      this.props.color = this.props.allColors[index];
    }
  }
  
  getRandomColor() {
    const colorIndex = Math.floor(Math.random() * this.props.allColors.length);
    return this.props.allColors[colorIndex];
  }
  
  render() {

    return (
      <div className="App">
        {this.state.boxes.map((color, i) => (<Box key={i} index={i} color={color} changeColor={this.changeColor} />))}
      </div>
    );
  }
}

